let dia = 1;

switch (dia) {
    case 0:
        console.log("Domingo");
        break;
    case 1:
        console.log("lunes");
        break;
    case 2:
        console.log("martes");
        break;
    case 3:
        console.log("miercoles");
        break;
    case 4:
        console.log("jueves");
        break;


}